class CoffeeModel {
  final int id;
  final String name;
  final String imagePath;
  final String description;
  final double price;
  final bool isFavorit;

  CoffeeModel({
    required this.id,
    required this.name,
    required this.imagePath,
    required this.description,
    required this.price,
    required this.isFavorit,
  });
}
